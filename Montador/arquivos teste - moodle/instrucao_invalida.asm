
;Programa que transforma um número decimal
;a binário
SECTION TEXT
INPUT OLD_DATA
LOAD OLD_DATA
L1: DIV DOIS
STORE  NEW_DATA
MULT DOIS
STR TMP_DATA
NEW_DATA: SPACE
LOAD OLD_DATA
SUB TMP_DATA
STR TMP_DATA
OUTPUT TMP_DATA
COPY NEW_DATA, OLD_DATA
LOAD OLD_DATA
JMPP L1
STOP
SECTION DATA
DOIS: CONST 2
OLD_DATA: SPACE
TMP_DATA: SPACE
